c = [1; 2; 3];

## x is a column
f = @(x)[
  -c(1).*x(1,:)+c(3).*x(2,:).*x(3,:), 
  c(1).*x(1,:) - c(2).*x(2,:).^2 - c(3).*x(2,:).*x(3,:), 
  c(2).*x(2,:).^2
];
  
Df = @(x)[
  -c(1), c(3)*x(3), c(3)*x(2);
  c(1), -2*c(2)*x(2) - c(3)*x(3), -c(3)*x(2);
  0, 2*c(2)*x(2), 0
];

x0 = [1;0;0];
I = [0, 1];
tau = 0.1;
A = [[1]];
b = [1];
TOL = tau^2;
[x, t] = RungeKuttaNewton(f, Df, x0, I, tau, b, A, TOL)