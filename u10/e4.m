G = [200, 1600, 100, 3200, 1600];
cj = 10^-12;
c0 = 40e-12;
g = @(x) (exp(x/0.026) - 1) * 10^-6;
alpha = 0.99;
Vdd = 1;
Vin = @(t) 2 * sin(2 * pi * t) + 0.2 * sin(20 * pi * t);
E = [cj, 0, -cj, 0, 0; 
      0, c0, 0, -c0, 0;
      -cj, 0, 2*cj, -cj, 0;
      0, -c0, -cj, c0+cj, 0;
      0, 0, 0, 0, 0];
A = -1 .* [G(1), 0, 0, 0, 0; 
              0, G(2)+G(4), 0, -G(4), 0;
              0, 0, G(3), 0, 0;
              0, -G(4), 0, G(4), 0;
              0, 0, 0, 0, G(5)]; 
b1 = @(u) [(1-alpha) * g(u(1) - u(3));
           alpha * g(u(1) - u(3));
           -g(u(1) - u(3)) - g(u(4) - u(3));
           (1-alpha) * g(u(4) - u(3));
           alpha * g(u(4) - u(3))];
b2 = @(t) -1 .* [G(1) * Vin(t);
                 G(2) * Vdd;
                 0;
                 0;
                 G(5) * Vdd];
b = @(t, u) b1(u) + b2(t);

rhs = @(t, u) A * u + b(t, u);
options = odeset('Mass', E);
[t,u] = ode23t(rhs,[0,2],y0)